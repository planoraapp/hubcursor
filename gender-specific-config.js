// ===== FUNÇÕES DE UTILIDADE =====
function getGenderSpecificClothing(gender, category, rarity) {
    if (GENDER_SPECIFIC_CONFIG[category] && GENDER_SPECIFIC_CONFIG[category][gender]) {
        return GENDER_SPECIFIC_CONFIG[category][gender][rarity] || [];
    }
    return [];
}

function getAllGenderSpecificClothing(gender, category) {
    if (GENDER_SPECIFIC_CONFIG[category] && GENDER_SPECIFIC_CONFIG[category][gender]) {
        const allItems = [];
        Object.values(GENDER_SPECIFIC_CONFIG[category][gender]).forEach(items => {
            allItems.push(...items);
        });
        return allItems;
    }
    return [];
}

function isGenderSpecificItem(itemId, category, gender) {
    const items = getGenderSpecificClothing(gender, category, 'all');
    return items.includes(itemId);
}

