// ===== FUNÇÃO DE CONVERSÃO PARA O EDITOR =====
function convertToEditorFormat() {
    const convertedData = {};
    
    Object.entries(HABBO_COMPLETE_CATEGORIES).forEach(([categoryKey, categoryData]) => {
        // Inicializar estrutura de raridades
        convertedData[categoryKey] = {
            nonhc: [],
            hc: [],
            sell: [],
            nft: []
        };
        
        // Mapear itens para suas respectivas raridades
        categoryData.items.forEach(item => {
            const itemData = {
                id: item.id,
                name: item.name,
                type: item.type,
                rarity: item.rarity,
                figure: item.figure,
                color: item.figure.split('-')[2] || '7'
            };
            
            // Adicionar à raridade correta
            if (item.rarity === 'nonhc') {
                convertedData[categoryKey].nonhc.push(itemData);
            } else if (item.rarity === 'hc') {
                convertedData[categoryKey].hc.push(itemData);
            } else if (item.rarity === 'sell') {
                convertedData[categoryKey].sell.push(itemData);
            } else if (item.rarity === 'nft') {
                convertedData[categoryKey].nft.push(itemData);
            } else {
                // Padrão para nonhc se não especificado
                convertedData[categoryKey].nonhc.push(itemData);
            }
        });
    });
    
    return convertedData;
}

// ===== EXPORTAÇÃO PARA USO GLOBAL =====
if (typeof window !== 'undefined') {
    window.HABBO_COMPLETE_CATEGORIES = HABBO_COMPLETE_CATEGORIES;
    window.convertToEditorFormat = convertToEditorFormat;
}
