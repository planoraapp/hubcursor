// Função para gerar dados organizados por raridade
function generateCompleteClothingData() {
    const clothingData = {};
    
    Object.entries(HABBO_COMPLETE_MAPPING).forEach(([category, categoryData]) => {
        clothingData[category] = {
            nonhc: [],    // Roupas normais (não-HC)
            hc: [],       // Roupas de Habbo Club (HC)
            sell: [],     // Roupas vendáveis
            nft: []       // Roupas NFT
        };
        
        categoryData.items.forEach(item => {
            const clothingItem = {
                id: item.id,
                name: item.name,
                type: item.type,
                rarity: item.rarity,
                figure: item.figure,
                color: item.color
            };
            
            // Categorizar por raridade
            switch (item.rarity) {
                case 'nonhc':
                    clothingData[category].nonhc.push(clothingItem);
                    break;
                case 'hc':
                    clothingData[category].hc.push(clothingItem);
                    break;
                case 'sell':
                    clothingData[category].sell.push(clothingItem);
                    break;
                case 'nft':
                    clothingData[category].nft.push(clothingItem);
                    break;
                default:
                    clothingData[category].nonhc.push(clothingItem);
            }
        });
    });
    
    return clothingData;
}

// Função para buscar item por ID e tipo
function findItemById(type, id) {
    for (const [category, categoryData] of Object.entries(HABBO_COMPLETE_MAPPING)) {
        const item = categoryData.items.find(item => item.type === type && item.id === id);
        if (item) return item;
    }
    return null;
}

// Função para listar todos os itens de uma categoria
function getItemsByCategory(category) {
    return HABBO_COMPLETE_MAPPING[category]?.items || [];
}

// Função para listar todos os itens de uma raridade
function getItemsByRarity(rarity) {
    const items = [];
    Object.values(HABBO_COMPLETE_MAPPING).forEach(categoryData => {
        categoryData.items.forEach(item => {
            if (item.rarity === rarity) {
                items.push(item);
            }
        });
    });
    return items;
}

